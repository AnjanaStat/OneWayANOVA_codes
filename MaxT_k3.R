fun1<-function(n1,n2,n3,v1,v2,v3)
{
  g1<-rnorm(n1,0,sqrt(v1));g2<-rnorm(n2,0,sqrt(v2));g3<-rnorm(n3,0,sqrt(v3))
  X1=mean(g1);X2=mean(g2);X3=mean(g3)
  S1=var(g1);S2=var(g2);S3=var(g3)
  v1<-sqrt(S1/n1+S2/n2);v2<-sqrt(S2/n2+S3/n3)
  T1=(X2-X1)/v1;T2=(X3-X2)/v2
  #Max-T test
  A=max(T1,T2,na.rm=FALSE)
  return(A)
}
fun2<-function(n1,n2,n3,v1,v2,v3)
{
  # find 500 max-T statistic values from fun1
  x<-replicate(1000,fun1(n1,n2,n3,v1,v2,v3))
  # arrange them in increasing order
  y<-sort(x,decreasing=FALSE)
  # gives the critical value
  c<-y[950]
  return(c)
}
