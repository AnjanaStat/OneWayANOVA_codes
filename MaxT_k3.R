fun4<-function(mu1,mu2,mu3,n1,n2,n3,v11,v12,v13)
{
  # find number of times maxt-T value > critical value among 1000 values
  out<-replicate(1000,fun3(mu1,mu2,mu3,n1,n2,n3,v11,v12,v13))
  p<-sum(out)/1000
  return(p)
}

